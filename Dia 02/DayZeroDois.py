#Formatando Strings
#String são variáveis do Tipo Texto
#Veja Abaixo como podemos Formata-las

Texto = "Cem dias Estudando Python"

print(Texto)                        #Exibe a mensagem sem formatação
print(Texto.upper())                #Converte A string para maiúsculas
print(Texto.lower())                #Converte para minusculas
print(Texto.swapcase())             #O que for maiúsculo vira minúsculo e vice-versa
print(Texto.title())                #Mostra cada inicial em Maiúscula
print(Texto.split())                #Converte em elemento de um Array cada palavra da String
print(Texto.capitalize())           #Iniciais em Maiúsculo
print(len(Texto))                   #Retorna a quantidade de Caracteres de uma String
print(Texto.replace('Cem', '100'))  #O Método replace substitui uma determinada palavra na String
